def write_empty_json(output_json: str, reason: str = ""):
    report = {
        "success_files": [],
        "timestamp": time.strftime("%Y-%m-%d %H:%M:%S"),
        "total_files": 0,
        "success_count": 0,
        "note": reason,
    }
    out = Path(output_json)
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(json.dumps(report, indent=2, ensure_ascii=False), encoding="utf-8")


async def async_main():
    parser = argparse.ArgumentParser(description="Sphinx gettext translation: docs/zh/ → locale/en/LC_MESSAGES/")
    parser.add_argument("--files", help="Comma-separated .pot file names (relative to locale/zh/LC_MESSAGES/)")
    parser.add_argument("--all", action="store_true", help="Translate all .pot files")
    parser.add_argument("--first-time", action="store_true",
                        help="First-time: run sphinx-build + translate ALL .pot files")
    parser.add_argument("--skip-gettext", action="store_true",
                        help="Skip sphinx-build -b gettext step (use existing .pot files)")
    parser.add_argument("--output-json", default=os.getenv("OUTPUT_JSON", "/tmp/translation_results.json"))
    parser.add_argument("--api-key", default=os.getenv("DEEPSEEK_API_KEY"))
    args = parser.parse_args()

    output_json = args.output_json

    api_key = args.api_key or os.getenv("DEEPSEEK_API_KEY")
    if not api_key:
        msg = "DEEPSEEK_API_KEY not set"
        print(f"Error: {msg}")
        write_empty_json(output_json, msg)
        return 1

    # Step 1: Generate .pot files via sphinx-build (unless --skip-gettext)
    if not args.skip_gettext:
        if not run_sphinx_gettext():
            write_empty_json(output_json, "sphinx-build -b gettext failed")
            return 1

    # Step 2: Determine which .pot files to translate
    if args.files:
        raw_files = [f.strip() for f in args.files.split(",") if f.strip()]
        pot_list = []
        for f in raw_files:
            p = Path(f)
            if not p.exists():
                p = POT_DIR / f
            if p.exists() and p.suffix == ".pot":
                if not _is_excluded(p):
                    pot_list.append(p)
    elif args.first_time:
        pot_list = find_all_pot_files()
        print(f"First-time translation: found {len(pot_list)} .pot file(s)")
    elif args.all:
        pot_list = find_changed_pot_files()
        print(f"Incremental translation: found {len(pot_list)} changed .pot file(s)")
    else:
        msg = "specify --files, --all, or --first-time"
        print(f"Error: {msg}")
        write_empty_json(output_json, msg)
        return 1

    if not pot_list:
        print("No .pot files to translate")
        write_empty_json(output_json, "no .pot files to translate")
        return 0

    translator = PoTranslator(api_key=api_key)
    return await translator.translate_files(pot_list, output_json)


if __name__ == "__main__":
    sys.exit(asyncio.run(async_main()))
