EXCLUDED_DIRS = ["python-api", "triton_api", "triton_api_extension", "libdevice"]


def _is_excluded(pot_path: Path) -> bool:
    """Check if a .pot file path falls under an excluded source directory."""
    try:
        rel = pot_path.relative_to(POT_DIR)
    except ValueError:
        return False
    # Check locations via the pot file content
    entries = parse_pot_file(pot_path)
    for entry in entries.values():
        for loc in entry.get("locations", []):
            if _is_excluded_location(loc, EXCLUDED_DIRS):
                return True
    return False


def find_all_pot_files() -> list[Path]:
    """Find all .pot files in locale/zh/LC_MESSAGES/ excluding those from excluded dirs."""
    result = []
    for pot_file in sorted(POT_DIR.rglob("*.pot")):
        if not _is_excluded(pot_file):
            result.append(pot_file)
    return result


def find_changed_pot_files() -> list[Path]:
    """Find .pot files that have new/changed entries compared to their .po counterpart.

    Uses git diff on .pot files if available (to detect which .pot files were regenerated),
    otherwise compares .pot and .po entry hashes directly.
    """
    # Try git diff first
    try:
        result = subprocess.run(
            ["git", "diff", "--name-only", "--diff-filter=ACMUX", "HEAD", "--", "locale/zh/LC_MESSAGES/"],
            capture_output=True,
            text=True,
            check=False,
        )
        if result.returncode == 0 and result.stdout.strip():
            changed_pots = []
            for line in result.stdout.strip().split("\n"):
                line = line.strip()
                if line.endswith(".pot"):
                    p = Path(line)
                    if not _is_excluded(p):
                        changed_pots.append(p)
            if changed_pots:
                return changed_pots
    except Exception:
        pass

    # Fallback: compare .pot entries with .po entries
    changed = []
    for pot_file in POT_DIR.rglob("*.pot"):
        if _is_excluded(pot_file):
            continue

        rel = pot_file.relative_to(POT_DIR)
        po_file = PO_DIR / rel
        if po_file.suffix != '.po':
            po_file = po_file.with_suffix('.po')

        pot_entries = parse_pot_file(pot_file)
        po_entries = parse_pot_file(po_file)

        # Check if any pot entry is missing or different in po
        for msgid, entry in pot_entries.items():
            existing = po_entries.get(msgid)
            if not existing or not existing.get("translated"):
                changed.append(pot_file)
                break

    return changed


def run_sphinx_gettext():
    """Run sphinx-build -b gettext to generate/update .pot files."""
    print("Running: sphinx-build -b gettext docs/zh locale/")
    result = subprocess.run(
        [sys.executable, "-m", "sphinx", "-b", "gettext", "-q",
         str(ZH_DIR), str(LOCALE_DIR)],
        capture_output=True,
        text=True,
    )
    if result.returncode != 0:
        print(f"sphinx-build failed:\n{result.stderr}")
        return False
    if result.stderr:
        print(f"  stderr: {result.stderr.strip()}")
    print("  Done")
    return True


