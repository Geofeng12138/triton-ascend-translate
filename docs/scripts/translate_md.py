"""
Sphinx gettext translation workflow for Chinese docs.

This script follows the standard Sphinx gettext i18n workflow:
  1. Run sphinx-build -b gettext to generate .pot files from docs/zh/
  2. Parse .pot files, compare with existing .po files (translation memory)
  3. Translate new/changed entries via DeepSeek API
  4. Save .po files to locale/en/LC_MESSAGES/
  5. (Sphinx build -D language=en will use .po files to render English HTML)

Directory layout:
    docs/zh/            Chinese source Markdown/RST files (input)
    locale/zh/LC_MESSAGES/   .pot template files (generated by sphinx-build -b gettext)
    locale/en/LC_MESSAGES/   .po translation files (generated by this script)

Usage:
    # Translate all .pot files that have new/changed content
    python translate_md.py --all

    # Translate specific .pot files
    python translate_md.py --files "quick_start.pot,FAQ.pot"
"""

import argparse
import asyncio
import hashlib
import json
import os
import re
import subprocess
import sys
import time
from pathlib import Path
from typing import Optional

from openai import AsyncOpenAI

# --- Directory Layout ---
ZH_DIR = Path("docs/zh")
LOCALE_DIR = Path("locale")
POT_DIR = LOCALE_DIR / "zh" / "LC_MESSAGES"
PO_DIR = LOCALE_DIR / "en" / "LC_MESSAGES"

SYSTEM_PROMPT = ("You are a professional technical documentation translation expert, "
                 "proficient in Chinese-to-English technical document translation.")

BLOCK_TRANSLATION_PROMPT = """Translate the following Chinese text into English.

Rules:
1. Return ONLY the translated text, no explanations, no markdown fences.
2. Use standard English technical terminology.
3. For proper nouns (person names, company names, product names), keep them as-is.
4. If the text contains inline code (`code`), leave the inline code content unchanged.
5. If any sentence is too difficult to translate, keep the original Chinese as-is.

Text to translate:
{content}"""

# ---------------------------------------------------------------------------
# PO / POT file parsing
# ---------------------------------------------------------------------------


def parse_pot_file(filepath: Path) -> dict:
    """
    Parse a .pot or .po file and return entries dict.

    Returns dict mapping msgid -> entry dict:
    {
        "msgid": str,
        "msgstr": str,
        "translated": bool,
        "locations": list[str],   # source file locations
    }
    """
    entries = {}
    if not filepath.exists():
        return entries

    raw = filepath.read_text(encoding="utf-8")

    # Split into entry blocks separated by blank lines
    blocks = raw.split('\n\n')

    for block in blocks:
        block = block.strip()
        if not block:
            continue

        # Skip header entry (msgid "" at start)
        if block.startswith('msgid ""') and '# ' not in block.split('\n')[0]:
            continue

        msgid = _extract_po_value(block, 'msgid')
        msgstr = _extract_po_value(block, 'msgstr')
        locations = _extract_locations(block)

        if msgid is not None:
            entries[msgid] = {
                "msgid": msgid,
                "msgstr": msgstr or "",
                "translated": bool(msgstr and msgstr.strip()),
                "locations": locations,
            }

    return entries


def _extract_po_value(block: str, field: str) -> Optional[str]:
    """Extract the value of a msgid/msgstr field from a PO block."""
    pattern = re.compile(
        rf'{field}\s+"((?:[^"\\]|\\.)*)"',
        re.MULTILINE,
    )
    match = pattern.search(block)
    if match:
        raw = match.group(1)
        return raw.replace('\\"', '"').replace('\\\\', '\\')

    # Multi-line variant
    if f'{field} ""' in block:
        # Collect all following "...\n" lines
        lines = block.split('\n')
        in_field = False
        parts = []
        for line in lines:
            if line.startswith(f'{field} ""'):
                in_field = True
                continue
            if in_field:
                # Match "content"
                m = re.match(r'\s*"((?:[^"\\]|\\.)*)"', line)
                if m:
                    parts.append(m.group(1).replace('\\"', '"').replace('\\\\', '\\'))
                else:
                    break
        if parts:
            return ''.join(parts)
        return ""

    return None


def _extract_locations(block: str) -> list[str]:
    """Extract file:line locations from a PO block."""
    locs = []
    for line in block.split('\n'):
        if line.startswith('#:'):
            locs.extend(line[2:].strip().split())
    return locs


def _is_excluded_location(location: str, excluded_dirs: list[str]) -> bool:
    """Check if a file location belongs to an excluded directory."""
    for d in excluded_dirs:
        if f"/{d}/" in location or location.startswith(f"{d}/"):
            return True
    return False


def write_po_file(filepath: Path, entries: dict, source_pot: str = ""):
    """Write entries dict to a .po file."""
    filepath.parent.mkdir(parents=True, exist_ok=True)

    now_str = time.strftime("%Y-%m-%d %H:%M%z")

    lines = []
    # Header
    lines.append(f'# English translations for triton-ascend docs.\n'
                 f'# Copyright (c) 2025 Huawei Technologies Co., Ltd.\n'
                 f'#\n'
                 f'msgid ""\n'
                 f'msgstr ""\n'
                 f'"Project-Id-Version: triton-ascend-docs\\n"\n'
                 f'"POT-Creation-Date: {now_str}\\n"\n'
                 f'"PO-Revision-Date: {now_str}\\n"\n'
                 f'"Last-Translator: Auto Translation (DeepSeek)\\n"\n'
                 f'"Language-Team: English\\n"\n'
                 f'"Language: en\\n"\n'
                 f'"MIME-Version: 1.0\\n"\n'
                 f'"Content-Type: text/plain; charset=UTF-8\\n"\n'
                 f'"Content-Transfer-Encoding: 8bit\\n"\n'
                 f'"Plural-Forms: nplurals=2; plural=(n != 1);\\n"\n')

    for entry in entries.values():
        msgid = entry["msgid"]
        msgstr = entry.get("msgstr", "")

        # Locations
        for loc in entry.get("locations", []):
            lines.append(f'#: {loc}')

        # Write msgid
        if '\n' in msgid:
            lines.append('msgid ""')
            for part in msgid.split('\n'):
                escaped = _escape_po(part)
                lines.append(f'"{escaped}\\n"')
        else:
            lines.append(f'msgid "{_escape_po(msgid)}"')

        # Write msgstr
        if '\n' in msgstr:
            lines.append('msgstr ""')
            for part in msgstr.split('\n'):
                escaped = _escape_po(part)
                lines.append(f'"{escaped}\\n"')
        else:
            lines.append(f'msgstr "{_escape_po(msgstr)}"')

        lines.append('')  # blank line separator

    text = '\n'.join(lines)
    filepath.write_text(text, encoding="utf-8")


def _escape_po(s: str) -> str:
    """Escape a string for PO format."""
    s = s.replace('\\', '\\\\')
    s = s.replace('"', '\\"')
    return s


# ---------------------------------------------------------------------------
# Translation engine
# ---------------------------------------------------------------------------


class PoTranslator:
    """Translate .pot entries to .po using DeepSeek API with translation memory."""

    def __init__(self, api_key: str):
        self.client = AsyncOpenAI(api_key=api_key, base_url="https://api.deepseek.com")

    async def translate_file(self, pot_path: Path) -> bool:
        """Translate a single .pot file, producing/updating the matching .po file."""
        rel = pot_path.relative_to(POT_DIR)
        po_path = PO_DIR / rel
        if po_path.suffix != '.po':
            po_path = po_path.with_suffix('.po')

        # Parse existing translations (if any)
        pot_entries = parse_pot_file(pot_path)
        po_entries = parse_pot_file(po_path)

        if not pot_entries:
            print(f"  Skip: {pot_path.name} (empty)")
            return False

        name = pot_path.stem
        print(f"  {name}: {len(pot_entries)} entries", end=" ", flush=True)

        # Determine which entries need translation
        new_entries = {}
        untranslated = []
        translations_kept = 0

        for msgid, entry in pot_entries.items():
            existing = po_entries.get(msgid)
            if existing and existing.get("translated"):
                # Translation memory hit - keep it
                new_entries[msgid] = existing
                translations_kept += 1
            else:
                # New or changed entry - needs translation
                new_entry = {
                    "msgid": msgid,
                    "msgstr": "",
                    "translated": False,
                    "locations": entry.get("locations", []),
                }
                new_entries[msgid] = new_entry
                untranslated.append((msgid, msgid))

        print(f"[{translations_kept} kept, {len(untranslated)} new]", end=" ", flush=True)

        # Translate new entries via API
        if untranslated:
            print(f"\n    Translating {len(untranslated)} entry(ies)...", end=" ", flush=True)
            for idx, (msgid, text) in enumerate(untranslated):
                translation = await self._translate_single(text, name)
                if translation:
                    new_entries[msgid]["msgstr"] = translation
                    new_entries[msgid]["translated"] = True
                if idx < len(untranslated) - 1:
                    await asyncio.sleep(0.3)
            print(" done", end=" ", flush=True)

        # Write .po file
        write_po_file(po_path, new_entries, str(pot_path))
        print("OK")
        return True

    async def _translate_single(self, content: str, context: str = "") -> Optional[str]:
        """Translate a single text string via DeepSeek API."""
        prompt = BLOCK_TRANSLATION_PROMPT.format(content=content)
        system = SYSTEM_PROMPT
        if context:
            system = f"{SYSTEM_PROMPT} (File: {context})"

        try:
            response = await self.client.chat.completions.create(
                model="deepseek-chat",
                messages=[
                    {"role": "system", "content": system},
                    {"role": "user", "content": prompt},
                ],
                max_tokens=8192,
                temperature=0.3,
            )
            text = response.choices[0].message.content
            return text.strip() if text else None
        except Exception as e:
            print(f"API error: {e}")
            return None

    async def translate_files(self, pot_list: list[Path], output_json: str) -> int:
        """Translate a list of .pot files sequentially."""
        print(f"Translating {len(pot_list)} .pot file(s)")

        success_files = []
        for pot_path in pot_list:
            ok = await self.translate_file(pot_path)
            if ok:
                rel = pot_path.relative_to(POT_DIR)
                po_path = PO_DIR / rel
                if po_path.suffix != '.po':
                    po_path = po_path.with_suffix('.po')
                success_files.append(str(po_path))

        print(f"\nResult: {len(success_files)}/{len(pot_list)} translated")

        report = {
            "success_files": success_files,
            "timestamp": time.strftime("%Y-%m-%d %H:%M:%S"),
            "total_files": len(pot_list),
            "success_count": len(success_files),
        }
        out = Path(output_json)
        out.parent.mkdir(parents=True, exist_ok=True)
        out.write_text(json.dumps(report, indent=2, ensure_ascii=False), encoding="utf-8")
        print(f"Results written to {output_json}")

        return 0 if success_files else 1


